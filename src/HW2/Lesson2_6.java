package HW2;

public class Lesson2_6 {
    public static void main(String[] args) {
        chekNumber(5);
    }

    public static void chekNumber(int number) {
        if (number >= 0)
            System.out.println("Число пложительное");
        else
            System.out.println("Число отрицательное");
    }
}
